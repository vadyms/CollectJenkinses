
public class Jenkins {
	
	private String jenkinsName;
	private String jenkinsAlias;
	private String jenkinsUrl;
	private String userName;
	private String userPassword;
	
	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}
	

	public String getUserPassword() {
		return userPassword;
	}
	

	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	

	public String getJenkinsName() {
		return jenkinsName;
	}
	
	public void setJenkinsName(String jenkinsName) {
		this.jenkinsName = jenkinsName;
	}
	
	public String getJenkinsAlias() {
		return jenkinsAlias;
	}
	
	public void setJenkinsAlias(String jenkinsAlias) {
		this.jenkinsAlias = jenkinsAlias;
	}
	
	public String getJenkinsUrl() {
		return jenkinsUrl;
	}
	
	public void setJenkinsUrl(String jenkinsUrl) {
		this.jenkinsUrl = jenkinsUrl;
	}
	
	
	
}
